#![no_std]

use soroban_sdk::{
    contract, contractimpl, contracttype, contracterror,
    Address, BytesN, Env, Vec, symbol_short,
    crypto::bn254::{Bn254G1Affine, Bn254G2Affine, Bn254Fr},
    U256,
};

const TREE_DEPTH: u32 = 20;

#[derive(Clone)]
#[contracttype]
pub enum DataKey {
    Admin,
    Denomination,
    MerkleRoot,
    CommitmentCount,
    Commitment(u32),
    Nullifier(BytesN<32>),
    ZeroHash(u32),
    FilledSubtree(u32),
}

#[contracterror]
#[derive(Clone, Debug, Copy)]
pub enum PoolError {
    AlreadyInitialized = 1,
    NullifierSpent = 2,
    InvalidProof = 3,
    MerkleTreeFull = 4,
    Unauthorized = 5,
    InvalidRoot = 6,
}

#[derive(Clone)]
#[contracttype]
pub struct Groth16Proof {
    pub a: BytesN<64>,
    pub b: BytesN<128>,
    pub c: BytesN<64>,
}

#[contract]
pub struct Zer0Pool;

#[contractimpl]
impl Zer0Pool {
    pub fn initialize(env: Env, admin: Address, denomination: i128) -> Result<(), PoolError> {
        if env.storage().instance().has(&DataKey::Admin) {
            return Err(PoolError::AlreadyInitialized);
        }
        admin.require_auth();
        env.storage().instance().set(&DataKey::Admin, &admin);
        env.storage().instance().set(&DataKey::Denomination, &denomination);
        env.storage().instance().set(&DataKey::CommitmentCount, &0u32);

        let zero = BytesN::<32>::from_array(&env, &[0u8; 32]);
        let mut current = zero.clone();
        for i in 0..TREE_DEPTH {
            env.storage().persistent().set(&DataKey::ZeroHash(i), &current);
            current = Self::poseidon_hash_2(&env, &current, &current);
        }
        env.storage().instance().set(&DataKey::MerkleRoot, &current);
        Ok(())
    }

    pub fn deposit(env: Env, from: Address, commitment: BytesN<32>) -> Result<u32, PoolError> {
        from.require_auth();
        let count: u32 = env.storage().instance().get(&DataKey::CommitmentCount).unwrap_or(0);
        if count >= 1_000_000 {
            return Err(PoolError::MerkleTreeFull);
        }
        env.storage().persistent().set(&DataKey::Commitment(count), &commitment);

        let mut current = commitment.clone();
        let mut idx = count;
        for i in 0..TREE_DEPTH {
            let sibling: BytesN<32> = if idx & 1 == 0 {
                let zero: BytesN<32> = env.storage().persistent().get(&DataKey::ZeroHash(i)).unwrap();
                env.storage().persistent().set(&DataKey::FilledSubtree(i), &current);
                zero
            } else {
                env.storage().persistent().get(&DataKey::FilledSubtree(i)).unwrap()
            };
            current = if idx & 1 == 0 {
                Self::poseidon_hash_2(&env, &current, &sibling)
            } else {
                Self::poseidon_hash_2(&env, &sibling, &current)
            };
            idx >>= 1;
        }
        env.storage().instance().set(&DataKey::MerkleRoot, &current);
        env.storage().instance().set(&DataKey::CommitmentCount, &(count + 1));
        env.events().publish((symbol_short!("deposit"), commitment, count), ());
        Ok(count)
    }

    pub fn withdraw(
        env: Env,
        to: Address,
        proof: Groth16Proof,
        nullifier_hash: BytesN<32>,
        root: BytesN<32>,
    ) -> Result<(), PoolError> {
        // ZK proof is the authorization — recipient need not sign (payroll / relayer flow)
        let spent: bool = env.storage().persistent().get(&DataKey::Nullifier(nullifier_hash.clone())).unwrap_or(false);
        if spent { return Err(PoolError::NullifierSpent); }
        let stored_root: BytesN<32> = env.storage().instance().get(&DataKey::MerkleRoot).unwrap();
        if root != stored_root { return Err(PoolError::InvalidRoot); }

        let valid = Self::verify_groth16(&env, &proof, &[root, nullifier_hash.clone()]);
        if !valid { return Err(PoolError::InvalidProof); }

        env.storage().persistent().set(&DataKey::Nullifier(nullifier_hash.clone()), &true);
        let denomination: i128 = env.storage().instance().get(&DataKey::Denomination).unwrap();
        let pool_addr = env.current_contract_address();
        env.events().publish((symbol_short!("withdraw"), nullifier_hash, to.clone()), ());
        let sac = soroban_sdk::token::Client::new(
            &env,
            &Address::from_string(&soroban_sdk::String::from_str(
                &env, "CDLZFC3SYJYDZT7K67VZ75HPJVIEUVNIXF47ZG2FB2RMQQVU2HHGCYSC",
            )),
        );
        sac.transfer(&pool_addr, &to, &denomination);
        Ok(())
    }

    pub fn get_root(env: Env) -> BytesN<32> {
        env.storage().instance().get(&DataKey::MerkleRoot).unwrap()
    }
    pub fn get_commitment_count(env: Env) -> u32 {
        env.storage().instance().get(&DataKey::CommitmentCount).unwrap_or(0)
    }
    pub fn get_commitment(env: Env, index: u32) -> Option<BytesN<32>> {
        env.storage().persistent().get(&DataKey::Commitment(index))
    }
    pub fn get_denomination(env: Env) -> i128 {
        env.storage().instance().get(&DataKey::Denomination).unwrap()
    }
    pub fn is_nullifier_spent(env: Env, nullifier_hash: BytesN<32>) -> bool {
        env.storage().persistent().get(&DataKey::Nullifier(nullifier_hash)).unwrap_or(false)
    }

    fn poseidon_hash_2(env: &Env, a: &BytesN<32>, b: &BytesN<32>) -> BytesN<32> {
        let a_u256 = U256::from_be_bytes(env, a.as_ref());
        let b_u256 = U256::from_be_bytes(env, b.as_ref());
        let inputs = Vec::from_array(env, [a_u256, b_u256]);
        let hash = soroban_poseidon::poseidon_hash::<3, Bn254Fr>(env, &inputs);
        hash.to_be_bytes().try_into().unwrap()
    }

    fn verify_groth16(env: &Env, proof: &Groth16Proof, public_inputs: &[BytesN<32>]) -> bool {
        let bn = env.crypto().bn254();
        let a = Bn254G1Affine::from_bytes(proof.a.clone());
        let b = Bn254G2Affine::from_bytes(proof.b.clone());
        let c = Bn254G1Affine::from_bytes(proof.c.clone());
        let neg_a = -a;

        let vk_alpha = Bn254G1Affine::from_bytes(BytesN::<64>::from_array(env, &VK_ALPHA));
        let vk_beta = Bn254G2Affine::from_array(env, &VK_BETA);
        let vk_gamma = Bn254G2Affine::from_array(env, &VK_GAMMA);
        let vk_delta = Bn254G2Affine::from_array(env, &VK_DELTA);

        let ic0 = Bn254G1Affine::from_bytes(BytesN::<64>::from_array(env, &VK_IC0));
        let ic1 = Bn254G1Affine::from_bytes(BytesN::<64>::from_array(env, &VK_IC1));
        let ic2 = Bn254G1Affine::from_bytes(BytesN::<64>::from_array(env, &VK_IC2));

        let input0 = Bn254Fr::from_bytes(public_inputs[0].clone());
        let input1 = Bn254Fr::from_bytes(public_inputs[1].clone());

        let vk_x = {
            let t1 = bn.g1_mul(&ic1, &input0);
            let t2 = bn.g1_mul(&ic2, &input1);
            bn.g1_add(&bn.g1_add(&ic0, &t1), &t2)
        };

        let g1_vec = Vec::from_array(env, [neg_a, vk_alpha, vk_x, c]);
        let g2_vec = Vec::from_array(env, [b, vk_beta, vk_gamma, vk_delta]);
        bn.pairing_check(g1_vec, g2_vec)
    }
}

